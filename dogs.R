# Fonction de LogVraisemblance
LogVraisemblance <- function(alpha, beta, Y) {
  Vraisemblance <- 0
  for (i in 1:Ndogs) {
    xa <- 0
    for (j in 2:Ntrials) {
      xs <- sum(Y[i, 1:(j - 1)])
      p <- exp(alpha * xs + beta * (j - 1 - xs))
      Vraisemblance <- Vraisemblance + Y[i, j] * log(p) + (1 - Y[i, j]) * log(1 - p)
    }
  }
  return(Vraisemblance)
}

